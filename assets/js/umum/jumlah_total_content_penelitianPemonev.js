$("#total_nilai_luaran_wajib").click(function(){
  var ket1_nilai = parseInt($("#ket1_nilai").val());
  var ket2_nilai = parseInt($("#ket2_nilai").val());
  var ket3_nilai = parseInt($("#ket3_nilai").val());
  var ket4_nilai = parseInt($("#ket4_nilai").val());
  var ket5_nilai = parseInt($("#ket5_nilai").val());
  var tambahan1_nilai = parseInt($("#tambahan1_nilai").val());
  var tambahan2_nilai = parseInt($("#tambahan2_nilai").val());
  var tambahan3_nilai = parseInt($("#tambahan3_nilai").val());
  var tambahan4_nilai = parseInt($("#tambahan4_nilai").val());
  var total = ket1_nilai + ket2_nilai + ket3_nilai + ket4_nilai + ket5_nilai + tambahan1_nilai + tambahan2_nilai + tambahan3_nilai + tambahan4_nilai;
  $("#total_nilai_luaran_wajib").val(total);
});

